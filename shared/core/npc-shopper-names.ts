const SHOPPER_MIDDLE_INITIALS = "ABCDEFGHIJKLMNOPQRSTUVWXYZ".split("");

function randomIntWithRng(rng: () => number, min: number, max: number): number {
  return Math.floor(min + rng() * (max - min + 1));
}

export function makeNpcShopperName(rng: () => number, usedNames?: Set<string>): string {
  const maxUniquePairs = SHOPPER_FIRST_NAMES.length * SHOPPER_LAST_NAMES.length;
  const attempts = Math.max(16, Math.min(maxUniquePairs, 128));

  for (let attempt = 0; attempt < attempts; attempt += 1) {
    const first = SHOPPER_FIRST_NAMES[randomIntWithRng(rng, 0, SHOPPER_FIRST_NAMES.length - 1)];
    const last = SHOPPER_LAST_NAMES[randomIntWithRng(rng, 0, SHOPPER_LAST_NAMES.length - 1)];
    const name = `${first} ${last}`;
    if (!usedNames || !usedNames.has(name)) {
      usedNames?.add(name);
      return name;
    }
  }

  const first = SHOPPER_FIRST_NAMES[randomIntWithRng(rng, 0, SHOPPER_FIRST_NAMES.length - 1)];
  const middle = SHOPPER_MIDDLE_INITIALS[randomIntWithRng(rng, 0, SHOPPER_MIDDLE_INITIALS.length - 1)];
  const last = SHOPPER_LAST_NAMES[randomIntWithRng(rng, 0, SHOPPER_LAST_NAMES.length - 1)];
  const fallbackName = `${first} ${middle}. ${last}`;
  usedNames?.add(fallbackName);
  return fallbackName;
}